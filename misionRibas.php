    <h3 class="large-12 columns rojo">¿Qué es la Misión Ribas?</h3>
    <p class="tJustify">Es un programa social implementado por el Gobierno Nacional cuyo objetivo es reinsertar dentro de un sistema educativo y productivo a todas aquellas personas que no culminaron sus estudios de la tercera etapa de educación media y diversificada, para que obtengan su titulo de bachiller integral avalado por el Ministerio de Educación y Deportes.</p>
    <p class="tJustify">La política educativa de la Misión, comtempla la estrategia metodológica de las videoclases con un facilitador o facilitadora, lo cual permite la integración de áreas del conocimiento con una visión integral. Esta política se rige bajo las líneas y principios de inclusión, permanencia, cooperación y solidaridad.</p>
    <p class="tJustify">En el desarrollo del componente comunitario sociolaboral, se materializa el proceso educativo con una lógica distinta a la capitalista. Es la proyección del potencial creativo de vencedores y vencedoras sobre el entorno, impactando y transformando la realidad, organizándose bajo valores de convivencia, autoderminación y soberanía popular, formulando proyectos comunitarios integrales en función de las necesidades, potencialidades y vocaciones de la comunidad, realizando obras sociales y productivas en el marco de la formación e incorporación al sistema de economía social solidaria en construcción, hacia el desarrollo endógeno integral soberano.</p>
    <p class="tJustify">La formación de la ciudadanía en el marco de la refundación de la República se cristaliza en las discusiones y reflexiones que hacen los vencedores y vencedoras, esto permite la participación y formación sobre procesos políticos, sociales, culturales, económicos y ambientales que implican la calidad de vida presentes y futuras generaciones.</p>
